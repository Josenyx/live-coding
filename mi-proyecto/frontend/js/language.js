window.currentLang = document.getElementById("language") ? document.getElementById("language").value : "es";

window.t = function (key, params = {}) {
    let text = translations[window.currentLang][key] || key;
    for (const [k, v] of Object.entries(params)) {
        text = text.replace(`{${k}}`, v);
    }
    return text;
};

function updateDOMTranslations() {
    document.querySelectorAll("[data-lang]").forEach(el => {
        const key = el.getAttribute("data-lang");
        if (translations[window.currentLang][key]) {
            // Si el elemento es un input con placeholder, mejor actualizar el placeholder (opcional si lo pusiéramos)
            // Para mantenerlo simple, solo textContent
            el.textContent = translations[window.currentLang][key];
        }
    });

    // Actualizar placeholders específicos manualmente si es necesario
    const placeholders = [
        { id: "modalEmail", es: "ejemplo@correo.com", en: "user@email.com" },
        { id: "modalPassword", es: "Ej: Password123", en: "Ex: Password123" },
        { id: "registerEmail", es: "ejemplo@correo.com", en: "user@email.com" },
        { id: "registerPassword", es: "Ej: Password123 (Min 8 caract.)", en: "Ex: Password123 (Min 8 chars)" },
        { id: "registerConfirmPassword", es: "Repite tu contraseña", en: "Repeat password" },
        { id: "discount-code", es: "XXXX-XXXX-XXXX", en: "XXXX-XXXX-XXXX" },
        { id: "sh-nombre", es: "Ej: Carlos", en: "Ex: John" },
        { id: "sh-apellidos", es: "Ej: García López", en: "Ex: Doe Smith" },
        { id: "sh-direccion", es: "Ej: Calle Mayor 12, 3ºB", en: "Ex: 123 Main St, Apt 4B" },
        { id: "sh-ciudad", es: "Ej: Madrid", en: "Ex: New York" },
        { id: "sh-provincia", es: "Ej: Madrid", en: "Ex: NY" },
        { id: "sh-pais", es: "Ej: España", en: "Ex: USA" }
    ];

    placeholders.forEach(p => {
        const el = document.getElementById(p.id);
        if (el) el.placeholder = p[window.currentLang];
    });

    // Si la función global existe (por si products.js la expone), redibujamos el carrito para actualizar el idioma
    if (typeof renderCartDrawer === "function") {
        renderCartDrawer();
    }
}

document.getElementById("language").addEventListener("change", (e) => {
    window.currentLang = e.target.value;
    updateDOMTranslations();
});

// Inicializar a la carga
document.addEventListener("DOMContentLoaded", updateDOMTranslations);